.386P
.MODEL FLAT, STDCALL

; Внешние процедуры
EXTERN PROC1@0:NEAR

; Сегмент данных
_DATA SEGMENT PARA PUBLIC FLAT 'DATA'
_DATA ENDS

; Сегмент кода
_CODE SEGMENT PARA PUBLIC FLAT 'CODE'

; return 10
RET_10 PROC
	MOV EAX,10
	RET
RET_10 ENDP


START:
	CALL RET_10

	CALL PROC1@0

	RET
_CODE ENDS
END START