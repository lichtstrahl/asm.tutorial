.386P
.MODEL FLAT, STDCALL

; Внешние процедуры
EXTERN PROC1@0:NEAR

; Сегмент данных
_DATA SEGMENT PARA PUBLIC USE32 'DATA'
_DATA ENDS

; Сегмент кода
_CODE SEGMENT DWORD PUBLIC USE32 'CODE'

; return 10
RET_10 PROC
	MOV EAX,10
	RET
RET_10 ENDP


START:
	CALL RET_10

	JMP PROC1@0

	RET
_CODE ENDS
END START